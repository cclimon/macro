# ── Data ──────────────────────────────────────────────────────────────────────

@st.cache_data(ttl=3600, show_spinner="Loading margin data…")
def load_data() -> pd.DataFrame:
    p = Path(__file__).parent / "margin_data.csv"
    if not p.exists():
        raise FileNotFoundError(str(p))
    df = pd.read_csv(p, index_col=0, parse_dates=True)
    df.index = pd.to_datetime(df.index)
    return df


try:
    df = load_data()
except FileNotFoundError:
    st.error("**margin_data.csv not found.** Run `python fetch_data.py` first to generate the dataset.")
    st.stop()

