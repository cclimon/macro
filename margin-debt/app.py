# ── Chart builder ─────────────────────────────────────────────────────────────

def make_chart(
    series: pd.Series,
    color: str,
    fill_color: str,
    y_prefix: str = "",
    y_suffix: str = "",
    y_fmt: str = ".2f",
    filled: bool = False,
) -> go.Figure:
    fig = go.Figure()

    # Event vertical lines
    for date_str, label in EVENTS:
        dt = pd.Timestamp(date_str)
        if dff.index[0] <= dt <= dff.index[-1]:
            fig.add_vline(
                x=dt,
                line_color="rgba(255,255,255,0.06)",
                line_width=1,
                line_dash="dot",
                annotation=dict(
                    text=label,
                    font=dict(size=10, color="rgba(255,255,255,0.45)", family=SANS),
                    bgcolor="rgba(0,0,0,0)",
                    borderwidth=0,
                    yref="paper", y=1.0,
                    yanchor="top",
                    xanchor="left",
                    showarrow=False,
                ),
            )

    s = series.dropna()
    fig.add_trace(go.Scatter(
        x=s.index,
        y=s.values,
        mode="lines",
        line=dict(color=color, width=2),
        fill="tozeroy" if filled else "none",
        fillcolor=fill_color if filled else None,
        connectgaps=True,
        hovertemplate=(
            f"<b>%{{x|%b %Y}}</b><br>"
            f"{y_prefix}%{{y:{y_fmt}}}{y_suffix}<extra></extra>"
        ),
    ))

    tick_fmt = f"{y_fmt}" if not y_prefix and not y_suffix else y_fmt

    fig.update_layout(
        paper_bgcolor=BG,
        plot_bgcolor=CARD,
        height=220,
        margin=dict(t=4, b=4, l=4, r=52),
        showlegend=False,
        hovermode="x unified",
        font=dict(color=FG, family=SANS),
        xaxis=dict(
            showgrid=False,
            tickfont=dict(size=11, color="#ffffff", family=SANS),
            tickformat="%Y" if (years is None or years >= 10) else "%b %y",
            dtick="M12" if (years is None or years >= 5) else "M3",
            linecolor=BORDER,
        ),
        yaxis=dict(
            side="right",
            showgrid=True,
            gridcolor=BORDER,
            tickfont=dict(size=11, color="#ffffff", family=SANS),
            tickformat=tick_fmt,
            tickprefix=y_prefix,
            ticksuffix=y_suffix,
            linecolor=BORDER,
        ),
    )
    return fig


